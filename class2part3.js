let Days=11
switch(Days){
    case 1:
        console.log('sunday')
        break;
    case 2:
        console.log('monday')
        break;
    case 3:
        console.log('tuesday')
        break
    case 4:
        console.log('wednesday')
        break
    case 4:
        console.log('thursday')
        break
    case 5:
        console.log('friday')
        break
    case 6:
        console.log('saturday')
    default:
        console.log('invalid day')
        break;
}